#include <iostream>
using namespace std;
int main()
// Relational Operator -> true or false
{
    int a = 3;
    int b = 5;
    cout<<(a>b)<<endl; //false -> 0
    cout<<(a<b)<<endl; //True -> 1
    cout<<(a>=b)<<endl; // false -> 0
    cout<<(a<=b)<<endl; //true -> 1
    cout<<(a==b)<<endl; //false -> 0
    cout<<(a!=b)<<endl; //true -> 1
    return 0;
}