#include <iostream>
using namespace std;
int main(){
    //Input a
    int a;
    cout<<"Enter the value of a : ";
    cin>>a;
    //input b
    int b;
    cout<<"Enter the value of b : ";
    cin>>b;
    //Calculate sum
    int sum;
    sum = a + b;
    cout<<"Sum of a & b = "<<sum<<endl;

    //Calculate Product
    int product;
    product = a * b; // * = asterisk
    cout<<"Product of a & b = "<<product<<endl;
    return 0;
}